public class Customer {
    String Name;
    String Account_type;
    char Gender;
    String Date_of_birth;
    String Father_name;
    Customer(String Name,String Account_type, char Gender,String Date_of_birth,String Father_name){
      this.Name = Name;
      this.Account_type = Account_type;
      this.Gender = Gender;
      this.Date_of_birth = Date_of_birth;
      this.Father_name = Father_name;
    }
}

public class Product {
    static String name;
    static String brandName;
    static String version;
    static double weight;
    static String dimensions;
    static String simSlot;
    static String cardSlot;
    static String battery;
    static double price;

    public Product(String name, String brandName, String version, double weight, String dimensions, String simSlot, String cardSlot, String battery, double price) {
        this.name = name;
        this.brandName = brandName;
        this.version = version;
        this.weight = weight;
        this.dimensions = dimensions;
        this.simSlot = simSlot;
        this.cardSlot = cardSlot;
        this.battery = battery;
        this.price = price;
    }

}
